package main

import "fmt"

const (
	Years_in_month         = 12
	Pi             float64 = 3.14
)

func main() {

	fmt.Println("The value of the constants years in month and pi are ", Years_in_month, Pi)
	fmt.Printf("The type of years in month is %T\n", Years_in_month)
	fmt.Printf("The type of Pi is %T\n", Pi)

}
